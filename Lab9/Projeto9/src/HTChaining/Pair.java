package HTChaining;

public class Pair {
	
    private int chave;
    private String valor;
    
    public Pair(int chave, String valor) {
    	this.chave = chave;
    	this.valor = valor;
    }
    
    public int getChave() {
        return this.chave;
    }

    public String getValor() {
        return this.valor;
    }

}
